				<h1 class="title">Mario podría ganar el Óscar por su canción de “Luigi” en Super Marios Bros. La Película</h1>
				<p class="paragraph">
					Aún no ha pasado ni una semana y Nintendo, en colaboración con Illumination, están arrasando en la taquilla mundial con el estreno de Super Mario Bros. La Película, pero esa no podría ser su única victoria, porque la película ya se perfila para tener al menos un Óscar y por qué no hasta llevárselo a casa.
				</p>
				<p class="paragraph">
					El día de hoy se reveló el video oficial de “Luigi”, canción original de la película animada que interpreta Mario, personaje que en su versión original es interpretado por Chris Pratt, y la misma que podría ser elegible para poder ser nominada en la categoría de mejor canción original, así lo confirmó Universal.
				</p>
				<article class="imgnoticia">
					<video src="imagenes/bowser.mp4" width="720" height="405" controls></video>
				</article>
				<p class="paragraph">
					Cabe resaltar que esta power ballad que habla sobre el amor y admiración que Mario le tiene al personaje de Luigi, no solo fue interpretada por Pratt, sino también está coescrita por él en conjunto con Aaron Horvath y Michael Jelenic, directores de la película, así como Eric Osmond y John Spiker.
				</p>
				<p class="paragraph">
					Desde el estreno de Super Marios Bros. La Película, uno de los personajes que más gustó fue precisamente Luigi, ya que pocos esperaban que su verdadera intención por ayudar a Mario se trataba de una gran amistad. Por lo cual, la canción se volvió un momento icónico de la trama, aunado a su coro que repite “Luigi” varias veces.
				</p>
				<p class="paragraph">
					Si habéis escuchado alguna vez el podcast 'Scriptnotes' de Craig Mazin y John August —que, como siempre, os recomiendo—, sabréis que el mundo de los créditos, los sindicatos y las compensaciones económicas en la industria estadounidense es un auténtico laberinto casi incomprensible para el común de los mortales, y esto sólo lo complica todo aún más. No obstante, aún no está todo decidido.
				</p>
				<p class="paragraph">
					¿Será nominada? Tendremos que esperar unos meses para saberlo.
				</p>

